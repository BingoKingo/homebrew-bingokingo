cask "font-naikai-font-tw" do
  version "1.89"
  sha256 :no_check

  url "https://github.com/max32002/naikaifont.git",
      verified:  "github.com/",
      branch:    "master",
      only_path: "tw"
  name "NaikaiFont"
  name "內海字體"
  homepage "https://max-everyday.com/2020/03/naikaifont/"

  depends_on formula: "fonttools"

  font "NaikaiFontTW.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        sleep 10 && \
        ls '#{staged_path}/tw/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/tw/NaikaiFontTW.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
