cask "font-hana-meatball" do
  version "1.1,2020-08-05"
  sha256 :no_check

  url "https://github.com/max32002/max-hana.git",
      branch:    "master",
      only_path: "ttf"
  name "Hana Meatball"
  name "花園肉丸"
  homepage "https://github.com/max32002/max-hana/"

  depends_on formula: "fonttools"

  font "HanaMeatball.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/ttf/'*.ttf \
        | grep -v 'HanaMeatball' | xargs rm -f && \
        ls '#{staged_path}/ttf/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/ttf/HanaMeatball.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
