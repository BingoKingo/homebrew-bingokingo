cask "font-fake-pearl" do
  version "1.2"
  sha256 :no_check

  url "https://github.com/max32002/FakePearl.git",
      branch:    "master",
      only_path: "tw"
  name "Fake Pearl"
  name "假粉圓體"
  homepage "https://github.com/max32002/FakePearl/"

  depends_on formula: "fonttools"

  font "FakePearl.ttc"

  preflight do
    system_command "/bin/bash", args: [
      "-c", <<~SHELL
        ls '#{staged_path}/tw/'*.ttf \
        | awk '{print "\\"" $0 "\\""}' \
        | xargs '#{HOMEBREW_PREFIX}/opt/fonttools/bin/fonttools' ttLib \
        -o '#{staged_path}/tw/FakePearl.ttc'
      SHELL
    ]
  end

  caveats do
    license "https://scripts.sil.org/OFL"
    <<~EOS
      You can preview font information generated by `otfinfo` at:
        #{__dir__}/#{token}.otfinfo
    EOS
  end
end
